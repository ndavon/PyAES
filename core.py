
def message_to_blocks(message):
    if len(message) < 16:
        message.extend([ 0 ] * (16 - len(message))) # black magic
        return [ message ]
    else:
        return [ message[:16] ] + message_to_blocks(message[16:]) if len(message) > 16 else [ message[:16 ] ]

